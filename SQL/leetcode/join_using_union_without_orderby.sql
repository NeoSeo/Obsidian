(SELECT name AS results
FROM MovieRating JOIN Users USING(user_id) -- ******* using 사용한 join 
GROUP BY name
ORDER BY COUNT(*) DESC, name
LIMIT 1)

UNION ALL

(SELECT title AS results
FROM MovieRating JOIN Movies USING(movie_id)
WHERE EXTRACT(YEAR_MONTH FROM created_at) = 202002
GROUP BY title
ORDER BY AVG(rating) DESC, title
LIMIT 1);


with cte as (
select user_id, count(*) as cnt
from MovieRating
group by user_id
)
, cte_1 as(
select movie_id, avg(rating) as avg_rate
from MovieRating
where date_format(created_at, '%Y-%m') = '2020-02'
group by movie_id
)
, cte_2 as (
  select max(avg_rate) as max_rate
  from cte_1
)


(
select name as results
from cte c
    join users u on u.user_id = c.user_id
where cnt in (select max(cnt) from cte)
order by name
limit 1
)

UNION all

(
select title as results
from cte_1 c1, cte_2 c2, movies m
where avg_rate = max_rate and m.movie_id = c1.movie_id
order by title
limit 1
)


